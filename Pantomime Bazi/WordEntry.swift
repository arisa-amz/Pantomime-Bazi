import Foundation

struct WordEntry: Identifiable {
    let id: UUID
    let text: String
    let category: WordCategory
    let points: Int
    let isCustom: Bool

    init(_ text: String, category: WordCategory, points: Int) {
        self.id = UUID()
        self.text = text
        self.category = category
        self.points = points
        self.isCustom = false
    }

    init(customText: String) {
        self.id = UUID()
        self.text = customText
        self.category = .food
        self.points = 9
        self.isCustom = true
    }

    func displayText(language: AppLanguage) -> String { text }
    var _english: String { text }
    var _persian: String { text }
}

enum AppLanguage: String, CaseIterable {
    case english = "EN"
    case persian = "FA"
    var isRTL: Bool { self == .persian }
    var flagEmoji: String { self == .english ? "🇺🇸" : "🇮🇷" }
    var label: String { self == .english ? "EN" : "فا" }
}

enum WordCategory: String, CaseIterable, Identifiable {
    case food = "Food & Drink"
    case movies = "Movies & TV"
    case famous = "Celebrities"
    case places = "LandMarks"
    case idioms = "Idioms"
    case city = "City & Country"
    case kids = "Kids"
    case animals = "Animals"
    case sports = "Sports"
    case jobs = "Jobs"
    case objects = "Objects"
    case tech = "Technology"
    var id: String { rawValue }
    var persianName: String {
        switch self {
        case .food: return "غذا و خوراکی"
        case .movies: return "فیلم و سریال"
        case .famous: return "مشاهیر"
        case .places: return "اماکن گردشگری"
        case .idioms: return "ضرب المثل"
        case .city: return "شهر و کشور"
        case .kids: return "کودکان"
        case .animals: return "حیوانات"
        case .sports: return "ورزشی"
        case .jobs: return "مشاغل"
        case .objects: return "اشیا"
        case .tech: return "تکنولوژی"
        }
    }
    var emoji: String {
        switch self {
        case .food: return "🍕"
        case .movies: return "🎬"
        case .famous: return "⭐"
        case .places: return "🗺️"
        case .idioms: return "💬"
        case .city: return "🌍"
        case .kids: return "🧸"
        case .animals: return "🐾"
        case .sports: return "⚽"
        case .jobs: return "👩‍💼"
        case .objects: return "🎒"
        case .tech: return "📱"
        }
    }
}

